fruits = ["apple", "banana", "cherry", "date"]

print("Iterating over a list:")
for fruit in fruits:
    print(f"Fruit: {fruit}")

student_info = {
    "name": "Alice",
    "age": 20,
    "grade": "A",
    "city": "New York"
}

print("\nIterating over a dictionary:")
for key, value in student_info.items():
    print(f"{key}: {value}")
